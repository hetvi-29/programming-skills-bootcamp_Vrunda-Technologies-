#include <stdio.h>
void main()
{
    int x = 10;
    float y = 12.40;
    double z = 123.567;
    char q = 'w';
    char r = '@';
    char p = '2';

    printf("%d is x\n", x);
    printf("%f is y\n", y);
    printf("%lf is z\n", z);
    printf("%c is q\n", q);
    printf("%c is r\n", r);
    printf("%c is p\n", p);
}
