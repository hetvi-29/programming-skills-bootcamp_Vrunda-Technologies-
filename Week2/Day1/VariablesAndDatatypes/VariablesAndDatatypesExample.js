/*

Note: 
That the var keyword is used to declare a variable globally or locally in a function. 
The let keyword is used to declare a variable in a block scope. 
The const keyword is used to declare a constant that cannot be reassigned a new value.

*/

// Declare a variable using var keyword
var age = 30;

// Declare a variable using let keyword
let name = "John";

// Declare a constant using const keyword
const PI = 3.14;

// Print the variables to the console
console.log("My name is " + name + " and I am " + age + " years old.");
console.log("The value of PI is " + PI);

// Different data types in JavaScript
let message = "Hello World!"; // string data type
let number = 10; // number data type
let isTrue = true; // boolean data type
let myArray = [1, 2, 3]; // array data type
let myObject = { name: "John", age: 30 }; // object data type
let myNull = null; // null data type
let myUndefined; // undefined data type

// Print the data types to the console
console.log(message);
console.log(number);
console.log(isTrue);
console.log(myArray);
console.log(myObject);
console.log(myNull);
console.log(myUndefined);
